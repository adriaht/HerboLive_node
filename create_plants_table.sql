CREATE DATABASE IF NOT EXISTS herbolive CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
USE herbolive;

CREATE TABLE IF NOT EXISTS plants (
  id INT AUTO_INCREMENT PRIMARY KEY,
  scientific_name VARCHAR(255) DEFAULT NULL,
  common_name VARCHAR(255) DEFAULT NULL,
  family VARCHAR(255) DEFAULT NULL,
  genus VARCHAR(255) DEFAULT NULL,
  species VARCHAR(255) DEFAULT NULL,
  description TEXT DEFAULT NULL,
  image_url TEXT DEFAULT NULL,
  images JSON DEFAULT NULL,
  growth_rate VARCHAR(64) DEFAULT NULL,
  hardiness_zones VARCHAR(128) DEFAULT NULL,
  height VARCHAR(64) DEFAULT NULL,
  width VARCHAR(64) DEFAULT NULL,
  type VARCHAR(128) DEFAULT NULL,
  foliage VARCHAR(128) DEFAULT NULL,
  pollinators VARCHAR(255) DEFAULT NULL,
  leaf VARCHAR(255) DEFAULT NULL,
  flower VARCHAR(255) DEFAULT NULL,
  ripen VARCHAR(255) DEFAULT NULL,
  reproduction VARCHAR(255) DEFAULT NULL,
  soils TEXT DEFAULT NULL,
  pH VARCHAR(64) DEFAULT NULL,
  preferences TEXT DEFAULT NULL,
  tolerances TEXT DEFAULT NULL,
  habitat TEXT DEFAULT NULL,
  habitat_range TEXT DEFAULT NULL,
  edibility VARCHAR(64) DEFAULT NULL,
  medicinal TEXT DEFAULT NULL,
  other_uses TEXT DEFAULT NULL,
  pfaf TEXT DEFAULT NULL,
  source VARCHAR(32) DEFAULT NULL,
  last_updated TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  INDEX idx_common (common_name),
  INDEX idx_scientific (scientific_name)
);
